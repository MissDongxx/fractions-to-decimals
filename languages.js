// 语言管理器
class LanguageManager {
    constructor() {
        this.currentLanguage = this.detectLanguage();
        this.init();
    }

    detectLanguage() {
        // 优先使用localStorage中保存的语言
        const savedLang = localStorage.getItem('longDivisionLang');
        if (savedLang && languages[savedLang]) {
            return savedLang;
        }

        // 其次使用浏览器语言
        const browserLang = navigator.language || navigator.userLanguage;
        
        // 精确匹配
        if (languages[browserLang]) {
            return browserLang;
        }

        // 模糊匹配（如 en-US 匹配 en）
        const langCode = browserLang.split('-')[0];
        for (const key in languages) {
            if (key.startsWith(langCode)) {
                return key;
            }
        }

        // 默认返回英文
        return 'en';
    }

    init() {
        this.createLanguageSelector();
        this.updateContent();
    }

    createLanguageSelector() {
        const header = document.querySelector('header');
        if (!header) return;

        const langSelector = document.createElement('div');
        langSelector.className = 'language-selector';
        langSelector.innerHTML = `
            <select id="languageSelect" class="language-select">
                ${Object.entries(languages).map(([code, lang]) => 
                    `<option value="${code}" ${code === this.currentLanguage ? 'selected' : ''}>
                        ${lang.flag} ${lang.name}
                    </option>`
                ).join('')}
            </select>
        `;

        header.appendChild(langSelector);

        // 添加事件监听器
        document.getElementById('languageSelect').addEventListener('change', (e) => {
            this.changeLanguage(e.target.value);
        });
    }

    changeLanguage(langCode) {
        if (!languages[langCode]) return;

        this.currentLanguage = langCode;
        localStorage.setItem('longDivisionLang', langCode);
        this.updateContent();
    }

    updateContent() {
        const translations = languages[this.currentLanguage].translations;

        // 更新页面标题
        document.title = translations.title;

        // 更新所有带有 data-i18n 属性的元素
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[key]) {
                if (element.tagName === 'INPUT' && element.type !== 'button' && element.type !== 'submit') {
                    element.placeholder = translations[key];
                } else {
                    // 使用innerHTML支持HTML标签
                    element.innerHTML = translations[key];
                }
            }
        });

        // 更新按钮值
        document.querySelectorAll('[data-i18n-value]').forEach(element => {
            const key = element.getAttribute('data-i18n-value');
            if (translations[key]) {
                element.value = translations[key];
            }
        });

        // 更新placeholder属性
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (translations[key]) {
                element.placeholder = translations[key];
            }
        });

        // 更新说明面板
        if (window.decimalToFractionDemo && window.decimalToFractionDemo.updateExplanation) {
            window.decimalToFractionDemo.updateExplanation();
        }
        
        // 更新分数转小数工具的说明面板
        if (window.longDivisionDemo && window.longDivisionDemo.updateExplanation) {
            window.longDivisionDemo.updateExplanation();
        }
    }

    t(key) {
        return languages[this.currentLanguage].translations[key] || key;
    }
}

// 全局语言管理器实例
let langManager;

// DOM加载完成后初始化
document.addEventListener('DOMContentLoaded', () => {
    langManager = new LanguageManager();
    // 将更新函数暴露到全局，以便 navigation.js 调用
    window.updateLanguage = () => langManager.updateContent();
});

// 全局函数：获取当前语言
function getCurrentLanguage() {
    return langManager ? langManager.currentLanguage : 'zh-CN';
}